# Даны значения зарплат из выборки выпускников: 100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150. Посчитать (желательно без использования статистических методов наподобие std, var, mean) среднее арифметическое, среднее квадратичное отклонение, смещенную и несмещенную оценки дисперсий для данной выборки.Даны значения зарплат из выборки выпускников: 100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150. Посчитать (желательно без использования статистических методов наподобие std, var, mean) среднее арифметическое, среднее квадратичное отклонение, смещенную и несмещенную оценки дисперсий для данной выборки.


# Заданная выборка
salaries = [100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150]

# Среднее арифметическое
mean = sum(salaries) / len(salaries)
print("Среднее арифметическое:", mean)

# Среднее квадратичное отклонение (стандартное отклонение)
std_dev = (sum((x - mean) ** 2 for x in salaries) / len(salaries)) ** 0.5
print("Среднее квадратичное отклонение (стандартное отклонение):", std_dev)

# Смещенная оценка дисперсии
var_biased = sum((x - mean) ** 2 for x in salaries) / len(salaries)
print("Смещенная оценка дисперсии:", var_biased)

# Несмещенная оценка дисперсии
var_unbiased = sum((x - mean) ** 2 for x in salaries) / (len(salaries) - 1)
print("Несмещенная оценка дисперсии:", var_unbiased)

